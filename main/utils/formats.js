/**
 *
 * @param {Date} dt
 * @param {String} style
 * @returns {String}
 */
export function formatDt(dt, style = null) {
	if (!style) return `<t:${dt.getTime()}>`;
	return `<t:${dt.getTime()}:${style}>`;
}
